# SELECT AND VIEW ALL ROWS
SELECT *
FROM portfolio_table;

# SELECT DISTINCT COMPANIES
SELECT DISTINCT company
FROM portfolio_table
LIMIT 3000;

# VIEW COMPANIES BEFORE AND AFTER TRIMMING UNWANTED CHARACTERS
SELECT company, TRIM(company)
FROM portfolio_table
LIMIT 3000;

# TRIM LEADING AND TRAILING SPACES IN COMPANY COLUMN
UPDATE portfolio_table
SET company = TRIM(company);
