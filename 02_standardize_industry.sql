FROM portfolio_table;

SELECT industry
FROM portfolio_table
WHERE industry LIKE '%-%';

# SELECT ROWS HAVING HYPHEN IN INDUSTRY
SELECT *
FROM portfolio_table
WHERE industry LIKE '%-%';

# SELECT ROWS HAVING CHARACTERS OTHER THAN ALPHABETS AND SPACES IN INDUSTRY
SELECT *
FROM portfolio_table
WHERE industry REGEXP '[^A-Za-z ]';

# SELECT ROWS HAVING CHARACTERS OTHER THAN ALPHABETS AND SPACES IN LOCATION
SELECT *
FROM portfolio_table
WHERE location REGEXP '[^A-Za_z -]';

# SELECT ROWS HAVING CHARACTERS OTHER THAN DECIMALS AND POINTS IN `percentage_laid_off`
SELECT *
FROM portfolio_table
WHERE percentage_laid_off REGEXP '[^0-9 .]';

# SELECT ROWS HAVING CHARACTERS OTHER THAN NUMBERS AND SLASH IN DATE
SELECT *
FROM portfolio_table 
WHERE `date` REGEXP '[^0-9/]';

# SELECT ONLY ROWS HAVING CHARACTERS OTHER THAN ALPHABETS AND SPACES IN STAGE COLUMN
SELECT *
FROM portfolio_table
WHERE stage REGEXP '[^A-Za-z ]';

# SELECT ROWS THAT HAVE CHARACTERS OTHER THAN ALPHABETS AND SPACE IN COUNTRY COLUMN_4 ROWS RETURNED
SELECT *
FROM portfolio_table
WHERE country REGEXP '[^A-Za-z ]';

# REMOVE TRAILING FULLSTOP FROM COUNTRY COLUMN_4 ROWS AFFECTED 
UPDATE portfolio_table
SET country = TRIM(TRAILING '.' FROM country );

